        // Attribuer un abonnement 
           public function attribuerAbonnement(Request $request)
{
    // ‚úÖ Validation des donn√©es re√ßues depuis le formulaire ou la requ√™te
    // V√©rifie que l'utilisateur (user_id) existe bien dans la table users
    // V√©rifie aussi que l'abonnement (abonnement_id) existe bien dans la table abonnements
    $request->validate([
        'user_id' => 'required|exists:users,id',
        'abonnement_id' => 'required|exists:abonnements,id',
    ]);

    // ‚úÖ On r√©cup√®re l'utilisateur concern√©
    $user = User::findOrFail($request->user_id);

    // ‚úÖ On r√©cup√®re l'abonnement choisi
    $abonnement = Abonnement::findOrFail($request->abonnement_id);

    // ‚úÖ Cr√©ation de la souscription dans la table "souscriptions"
    // Ici on dit : cet utilisateur a cet abonnement, avec une date de d√©but = maintenant
    // et une date de fin = maintenant + dur√©e d√©finie dans le mod√®le Abonnement
    Souscription::create([
        'user_id' => $user->id,
        'abonnement_id' => $abonnement->id,
        'date_debut' => now(),
        'date_fin' => now()->addDays($abonnement->duree), // Exemple : si dur√©e = 30 jours
    ]);

    // ‚úÖ Retourne en arri√®re avec un message de succ√®s
    return redirect()->back()->with('success', 'Abonnement attribu√© avec succ√®s.');
}


