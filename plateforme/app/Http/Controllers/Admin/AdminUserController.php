    public function update(Request $request, $id)
    {
        // 1. Validation des donn√©es
        $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users,email,' . $id,
            'role' => 'required|string|in:admin,user',
        ]);

        // 2. Trouver l'utilisateur
        $user = User::findOrFail($id);

        // 3. Mettre √† jour les attributs de l'utilisateur
        $user->name = $request->input('name');
        $user->email = $request->input('email');
        $user->role = $request->input('role');
        $user->save();

        // 4. Redirection avec un message de succ√®s
            return redirect()->route('admin.gestion_utilisateurs')->with('success', 'L\'utilisateur a √©t√© modifi√© avec succ√®s.');

    }



    public function store(Request $request)
{
    // 1. Validation des donn√©es du formulaire
    $request->validate([
        'name' => 'required|string|max:255',
        'email' => 'required|string|email|max:255|unique:users',
        'password' => 'required|string|min:8',
        'role' => ['required', Rule::in(['admin', 'user'])],
    ]);

    // 2. Cr√©ation de l'utilisateur
    User::create([
        'name' => $request->name,
        'email' => $request->email,
        'password' => Hash::make($request->password), // Crypte le mot de passe
        'role' => $request->role,
    ]);

    // 3. Redirection avec un message de succ√®s
    return redirect()->route('admin.gestion_utilisateurs')->with('success', 'Utilisateur ajout√© avec succ√®s !');
}



